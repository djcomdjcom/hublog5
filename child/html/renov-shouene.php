<h2 class="title " >基礎断熱</h2>

<div class="clearfix">

<div class="l-box w50">
  <h3 class="title noicon">基礎断熱とは？</h3>
  <p>
  基礎断熱とは、床下空間も室内空間のひとつとして考え、基礎内部をぐるっと断熱材で覆います。そして床下からの熱を伝わりにくくし、床下からの換気をなくし、床下空間を暖かく保つ工法です。<br>
  木造住宅は、根太・大引きのあいだに断熱材を落とし込む方法が簡単であったため、「床断熱」が昔から主流でしたが、北海道などでは、水道の凍結を防ぐという目的で、「基礎断熱」工法が開発され、普及しました。<br>
  基礎の立ち上がりで断熱をするために、床下の温度と同じになるので、基礎断熱には大いなるメリットがあります。
  </p>
</div><!--L-->

