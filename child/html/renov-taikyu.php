<h2 class="title ">耐久性</h2>

<article id="renov_taikyu_shiroari" class="clearfix anchor">

<h3 class="title ">シロアリ</h3>
<p>
  当社では、ベタ基礎の内断熱を採用し、ホウ酸系防蟻材によるシロアリ対策をしています。<br>
  新築注文住宅の場合は、被害写真を掲載してしまうと、不快感になってしまうため、 防蟻剤の製品写真や施行写真などを使用するようにしましょう。<br>
  シロアリは、太陽の光が当たらない暗い場所、風通しが悪くジメジメした湿度の高い床下などの人間の目につかないところで湿った木材を食害します。具体的には、お風呂場や洗面所、台所の床下など水回りに被害が集中することが多く、少しずつ確実に食害を進め、柱などの内部を空洞化してしまいます。
</p>

</article><!--renov_taikyu_shiroari-->


<article id="renov_taikyu_naibuketsuro" class="clearfix anchor">
<h3 class="title ">内部結露</h3>
<p>
  壁内の構造体が結露するとカビや腐朽菌の発生原因となり、構造体の強度が落ちてしまいます。結露は温度差によって空気中の水蒸気が水滴に変わる現象です。構造体の強度を維持するためには、壁内の構造体が一定の温度で保たれ空気に触れていない状態をつくることです。<br>
  気密シーリングや発砲ウレタンの吹きつけ断熱により、構造材を結露から守り、耐久性を向上させています。
</p>

</article><!--renov_taikyu_naibuketsuro-->


