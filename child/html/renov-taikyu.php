  ほかにも、湿気やシロアリによる腐食が考えられます。<br>
  これからさらに住み続けるためのリノベーションですので、建物を支える基礎の改修はもっとも大切な耐震工事といえます。<br>
  表面的な美観だけでなく長く住み続けるために、耐震性を重視した基礎の補強をいたしま
</p>

