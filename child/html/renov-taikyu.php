<h3 class="title ">耐震構造</h3>
<p>
  腐食している構造材はすべて取替えます。<br>
  長年住み続けた住宅の土台、柱は腐食が進み、ほとんど構造的な強度として役に立っていない構造材が多くみられます。<br>
  それらの構造材を全て取替えるか補強し、新築と同じ金物を使用して、現在の耐震基準に合った強い構造体へと生まれ変わらせます。
</p>

